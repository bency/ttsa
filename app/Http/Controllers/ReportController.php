        $data = [
            'report' => $report,
            'og_title' => date('Y-m-d', $report->reported_at) . ' ' . $report->title,
            'og_desc' => $report->content,
            'og_image' => $report->pic_url,
        ];
        return view('report.show', $data);
    }

